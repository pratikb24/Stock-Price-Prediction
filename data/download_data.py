"""
download_data.py
================
OPTIONAL: download real historical stock prices with yfinance.

This requires an internet connection and `pip install yfinance`. If you just
want to run the project offline, skip this and use the bundled
`sample_stock_data.csv` (generated by generate_sample_data.py).

Run:
    python data/download_data.py --ticker AAPL --start 2018-01-01 --end 2024-01-01
Produces:
    data/<TICKER>_stock_data.csv
"""

import os
import argparse


def download(ticker: str, start: str, end: str) -> str:
    try:
        import yfinance as yf
    except ImportError as e:
        raise SystemExit(
            "yfinance is not installed. Run `pip install yfinance` first."
        ) from e

    print(f"Downloading {ticker} from {start} to {end} ...")
    df = yf.download(ticker, start=start, end=end, auto_adjust=True)
    if df.empty:
        raise SystemExit(f"No data returned for {ticker}. Check the ticker/dates.")

    df = df.reset_index()
    # Flatten any MultiIndex columns that newer yfinance versions return
    df.columns = [c[0] if isinstance(c, tuple) else c for c in df.columns]
    keep = ["Date", "Open", "High", "Low", "Close", "Volume"]
    df = df[[c for c in keep if c in df.columns]]

    here = os.path.dirname(os.path.abspath(__file__))
    out_path = os.path.join(here, f"{ticker.upper()}_stock_data.csv")
    df.to_csv(out_path, index=False)
    print(f"[OK] Saved {len(df)} rows to {out_path}")
    return out_path


if __name__ == "__main__":
    p = argparse.ArgumentParser(description="Download real stock data via yfinance.")
    p.add_argument("--ticker", default="AAPL", help="Ticker symbol, e.g. AAPL")
    p.add_argument("--start", default="2018-01-01", help="Start date YYYY-MM-DD")
    p.add_argument("--end", default="2024-01-01", help="End date YYYY-MM-DD")
    args = p.parse_args()
    download(args.ticker, args.start, args.end)
